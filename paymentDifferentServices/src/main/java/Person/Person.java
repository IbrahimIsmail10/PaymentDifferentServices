package Person;

public class Person {
   public String username;
   public String password;
   public String email;
   public Person(){
	   this.username ="";
	   this.password="";
	   this.email ="";
   }
   public Person (String name,String pass,String email){
	   this.username =name;
	   this.password=pass;
	   this.email =email;
   }
 

}
